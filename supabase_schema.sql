ON public.blog_posts
FOR UPDATE
TO authenticated
USING ((auth.jwt() ->> 'email') = 'anya.shalaginova@gmail.com')
WITH CHECK ((auth.jwt() ->> 'email') = 'anya.shalaginova@gmail.com');

DROP POLICY IF EXISTS "Allow admin delete from blog_posts" ON public.blog_posts;
